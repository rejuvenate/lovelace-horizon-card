#!/usr/bin/env node
// Generates the localization index consumed by src/constants.ts and keeps the README
// language list in sync — both are fully derived from the JSON files in
// src/assets/localization/languages/. Each file declares its own English display name via
// a "languageName" key, so this script carries no per-language data and stays generic.
//
// Contributing a translation is just: add a `<code>.json` (with a "languageName") to that
// folder. Regeneration is an automatic part of the pull request (see the i18n-generate
// workflow), so contributors don't need to run anything — maintainers can run
// `yarn i18n:generate` locally to preview.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const languagesDir = join(root, 'src/assets/localization/languages')
const generatedFile = join(root, 'src/assets/localization/languages.generated.ts')
const readmeFile = join(root, 'README.md')

const README_START = '<!-- LANGUAGES:START (auto-generated by scripts/generate-i18n-index.mjs — do not edit) -->'
const README_END = '<!-- LANGUAGES:END -->'

const escapeRegExp = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
const identifierFor = (code) => code.replace(/[^a-zA-Z0-9]/g, '_')

const displayNames = new Intl.DisplayNames(['en'], { type: 'language' })
const nameFor = (code) => {
  // The English display name lives in the translation file itself, so this stays generic.
  const { languageName } = JSON.parse(readFileSync(join(languagesDir, `${code}.json`), 'utf8'))
  if (typeof languageName === 'string' && languageName.length > 0) {
    return languageName
  }
  // Fall back to Intl for a file that omits it, then to the raw code.
  const resolved = displayNames.of(code)
  return resolved && resolved !== code ? resolved : code
}

const codes = readdirSync(languagesDir)
  .filter((file) => file.endsWith('.json'))
  .map((file) => file.slice(0, -'.json'.length))
  .sort()

// --- 1. generated TypeScript index ---
const imports = codes
  .map((code) => `import ${identifierFor(code)} from './languages/${code}.json'`)
  .join('\n')
const entries = codes
  .map((code) => (identifierFor(code) === code ? code : `'${code}': ${identifierFor(code)}`))
  .join(', ')

const generated = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-i18n-index.mjs — do not edit by hand.
// Run \`yarn i18n:generate\` after adding or removing a language JSON file.
import type { THorizonCardI18NKeys } from '../../types'

${imports}

export const localizationLanguages: Record<string, THorizonCardI18NKeys> = { ${entries} }
`
writeFileSync(generatedFile, generated)

// --- 2. README language list ---
const list = codes.map((code) => `- \`${code}\` ${nameFor(code)}`).join('\n')
const block = `${README_START}\n${list}\n${README_END}`
const readme = readFileSync(readmeFile, 'utf8')
const blockRegex = new RegExp(`${escapeRegExp(README_START)}[\\s\\S]*?${escapeRegExp(README_END)}`)
if (!blockRegex.test(readme)) {
  throw new Error(`README language markers not found. Wrap the language list with:\n${README_START}\n${README_END}`)
}
writeFileSync(readmeFile, readme.replace(blockRegex, block))

console.log(`i18n: generated index and README list for ${codes.length} languages.`)
